# include "lcd.h"
# include "stdio.h"

u32 tick = 0;
char s[80];

void LED_init(void);
void LED_Set(u8 v);
extern const u8 pic[];
int main(void)
{
    u32 t0;
    u32 i;
    RCC->APB2ENR = 0x3D;
    while (1 &~ GPIOA->IDR);
    
    SysTick_Config(72000);
    STM3210B_LCD_Init();
    LED_init();
    
    LCD_DrawPicture(pic);
    
    LCD_SetBackColor(White);
    LCD_SetTextColor(Red);
    
    LCD_DisplayStringLine(Line1, " Hello? ");
    
    while (1)
    {
        if (tick-t0 >= 10)
        {
            t0 = tick;
            LED_Set(~(1<<(++i/20%8)));
            
            sprintf(s, " times: %.2lf s.", t0/1000.0);
            LCD_SetTextColor(Blue);
            LCD_DisplayStringLine(Line3, (u8*)s);
            
        }
    }
}



void SysTick_Handler(void)
{
    tick++;
}

